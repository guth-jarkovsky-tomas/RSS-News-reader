public class Article {
    String author;
    String title;
    String description;
    String url;
    String urlToImage;
    String published;

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public String getUrl() {
        return url;
    }

    public String getUrlToImage() {
        return urlToImage;
    }
}
